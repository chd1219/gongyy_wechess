string2map = function(board)
{
	var str = board.split(' ');
	var str1 = str[0];
	var map = [];
	var key = 0;
	var index = 0;
	str1 = str1.replace(/\//g, "");
	str1 = str1.replace(/2/g, "11");
	str1 = str1.replace(/3/g, "111");
	str1 = str1.replace(/4/g, "1111");
	str1 = str1.replace(/5/g, "11111");
	str1 = str1.replace(/6/g, "111111");
	str1 = str1.replace(/7/g, "1111111");
	str1 = str1.replace(/8/g, "11111111");
	str1 = str1.replace(/9/g, "111111111");
	
	var keys = {'k':7,'r':11,'c':12,'n':10,'b':9,'a':8,'p':13,'K':0,'R':4,'C':5,'N':3,'B':2,'A':1,'P':6};
	for (var i = 0; i < str1.length; i++)	{
		key = keys[str1[i]];
		if (key != undefined)
		{
			map[index++] = i;
			map[index++] = key;
		}	
	}
	return map;
}